print('세 개의 실수를 입력해주세요.')

num1 = float(input('정수 1 : ')) # 정수 1을 입력 받은 후, 입력 받은 문자를 실수형으로 변환해서 num1 변수에 저장
num2 = float(input('정수 2 : ')) # 정수 2를 입력 받은 후, 입력 받은 문자를 실수형으로 변환해서 num2 변수에 저장
num3 = float(input('정수 3 : ')) # 정수 3을 입력 받은 후, 입력 받은 문자를 실수형으로 변환해서 num3 변수에 저장
sum = num1 + num2 + num3 # num1, num2, num3를 더한 값을 sum 변수에 저장

print('이들의 합은', sum, '입니다.') # sum 값을 출력